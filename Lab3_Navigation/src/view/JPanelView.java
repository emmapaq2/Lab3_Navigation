    public void populateData(){
        this.txtFirstName2.setText(p1.getFname());
        txtLastName.setText(p1.getLname());
        txtAge.setText(p1.getAge());
        txtEmail.setText(p1.getEmail());
        
        String gender = p1.getGender();
        if (gender.equals("MALE")){
            grpGender.setSelected(this.rbMale.getModel(), true);
        } else if(gender.equals("FEMALE")){
            grpGender.setSelected(this.rbFemale.getModel(), true);
        } else{
           grpGender.setSelected( this.rbNotSay.getModel(), true); 
        }
        
    }

