    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed

        try {
            String fname = txtFirstName2.getText();
            String lname = txtLastName.getText();
            String age = txtAge.getText();
            String email = txtEmail.getText();
            String message_ = txtMsg.getText();

            String gender = grpGender.getSelection().getActionCommand();
            String pat_Type = cboPatientType.getSelectedItem().toString();
            String displayMessage ="";

            if(fname.trim() != "" || lname.trim() != "" ||  age.trim() != "" || email.trim() != "" || message_.trim() != "" || gender != "" || pat_Type != ""){
                displayMessage = "Name: "+ fname + " Gender: " + gender;
            }
           
            // System.out.println(this.txtName.getText());
            //System.out.println(grpGender.getSelection().getActionCommand());
            //JOptionPane.showMessageDialog(this, displayMessage);
            JOptionPane.showMessageDialog(this, displayMessage, "Success",HEIGHT, profilePicture);

        } catch (Exception ex){
            JOptionPane.showMessageDialog(this, ex.getMessage());
            //} catch (NullPointerException nex){
            //  JOptionPane.showMessageDialog(this, "Null entries not accepted!");
        }
    }//GEN-LAST:event_btnSubmitActionPerformed

    private void btnImageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnImageActionPerformed
        // TODO add your handling code here:
        JFileChooser file = new JFileChooser();
        if (file.showSaveDialog(null)== JFileChooser.APPROVE_OPTION){
            try {
                BufferedImage img = ImageIO.read(file.getSelectedFile());
                Image edited_image = img.getScaledInstance(250, 300, Image.SCALE_SMOOTH);

                if (edited_image != null ){
                    lblImage.setIcon(new ImageIcon(edited_image));
                    this.profilePicture = new ImageIcon(edited_image);
                }

            } catch(Exception ex) {
                JOptionPane.showMessageDialog(this, "Please upload image correctly","Error - Incorrect Image",JOptionPane.ERROR_MESSAGE);
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnImageActionPerformed

